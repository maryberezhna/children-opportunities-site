# -------------------- ОБЧИСЛЕННЯ ДАТ --------------------
def orthodox_easter(year: int) -> date:
    """Гаусова формула для Юліанської Пасхалії + 13 днів для відображення в Григоріанському."""
    a, b, c = year % 4, year % 7, year % 19
    d = (19 * c + 15) % 30
    e = (2 * a + 4 * b - d + 34) % 7
    month = (d + e + 114) // 31
    day = ((d + e + 114) % 31) + 1
    return date(year, month, day) + timedelta(days=13)


def nth_dow_of_month(year: int, month: int, n: int, dow_iso: int) -> date:
    """n-та поява weekday (ISO: Mon=1..Sun=7) у місяці."""
    d = date(year, month, 1)
    offset = (dow_iso - d.isoweekday()) % 7
    return d + timedelta(days=offset + 7 * (n - 1))


# -------------------- SLUG (UA → ASCII) --------------------
_UA_TRANSLIT = str.maketrans({
    "а": "a", "б": "b", "в": "v", "г": "h", "ґ": "g", "д": "d", "е": "e",
    "є": "ye", "ж": "zh", "з": "z", "и": "y", "і": "i", "ї": "yi", "й": "y",
    "к": "k", "л": "l", "м": "m", "н": "n", "о": "o", "п": "p", "р": "r",
    "с": "s", "т": "t", "у": "u", "ф": "f", "х": "kh", "ц": "ts", "ч": "ch",
    "ш": "sh", "щ": "shch", "ь": "", "ю": "yu", "я": "ya", "'": "", "’": "",
})

def slugify_ua(text: str) -> str:
    s = text.lower().translate(_UA_TRANSLIT)
    s = re.sub(r"[^a-z0-9\s-]", "", s)
    s = re.sub(r"\s+", "-", s).strip("-")
    return s[:80]


# -------------------- МОДЕЛЬ --------------------
Rule = (
    tuple[Literal["FIXED"], int, int]                # ("FIXED", month, day)
    | tuple[Literal["NTH_DOW"], int, int, int]       # ("NTH_DOW", month, n, dow_iso)
    | tuple[Literal["ORTHODOX_EASTER"], int]         # ("ORTHODOX_EASTER", delta_days)
)

@dataclass
class HolidayDef:
    emoji: str
    summary: str
    description: str
    rule: Rule
    category: Literal["Україна", "Міжнародні"]
    tags: list[str] = field(default_factory=list)

    @property
    def slug(self) -> str:
        return slugify_ua(self.summary)


def resolve_date(rule: Rule, year: int) -> date:
    """Обчислити конкретну дату події для року."""
    kind = rule[0]
    if kind == "FIXED":
        _, m, d = rule
        return date(year, m, d)
    if kind == "NTH_DOW":
        _, m, n, dow_iso = rule
        return nth_dow_of_month(year, m, n, dow_iso)
    if kind == "ORTHODOX_EASTER":
        _, delta = rule
        return orthodox_easter(year) + timedelta(days=delta)
    raise ValueError(f"Unknown rule kind: {kind}")


