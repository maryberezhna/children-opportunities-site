# -------------------- ГЕНЕРАЦІЯ .ICS --------------------
DOW_STR = {1: "MO", 2: "TU", 3: "WE", 4: "TH", 5: "FR", 6: "SA", 7: "SU"}

def build_ics() -> bytes:
    cal = Calendar()
    cal.add("prodid", "-//dityam.com.ua//Holiday Calendar 3.0//UK")
    cal.add("version", "2.0")
    cal.add("calscale", "GREGORIAN")
    cal.add("method", "PUBLISH")
    cal.add("x-wr-calname", "dityam.com.ua — Свята та дитячі дні")
    cal.add("x-wr-caldesc", "Українські державні свята + міжнародні дні, дотичні до дітей.")
    cal.add("x-wr-timezone", "Europe/Kyiv")

    for h in HOLIDAYS:
        title = f"{h.emoji} {h.summary}"
        desc = f"{h.description}\n\nКатегорія: {h.category}"

        if h.rule[0] == "FIXED":
            _, m, d = h.rule
            e = Event()
            e.add("uid", f"{h.slug}-fixed@dityam.com.ua")
            e.add("summary", title)
            e.add("description", desc)
            e.add("dtstart", date(CURRENT_YEAR, m, d))
            e.add("dtend", date(CURRENT_YEAR, m, d) + timedelta(days=1))
            e.add("categories", [h.category])
            e.add("rrule", {"freq": "yearly"})
            e.add("transp", "TRANSPARENT")
            cal.add_component(e)

        elif h.rule[0] == "NTH_DOW":
            _, m, n, dow_iso = h.rule
            start = nth_dow_of_month(CURRENT_YEAR, m, n, dow_iso)
            e = Event()
            e.add("uid", f"{h.slug}-nthdow@dityam.com.ua")
            e.add("summary", title)
            e.add("description", desc)
            e.add("dtstart", start)
            e.add("dtend", start + timedelta(days=1))
            e.add("categories", [h.category])
            e.add("rrule", {"freq": "yearly", "bymonth": [m], "byday": [f"{n}{DOW_STR[dow_iso]}"]})
            e.add("transp", "TRANSPARENT")
            cal.add_component(e)

        elif h.rule[0] == "ORTHODOX_EASTER":
            # Без RRULE — окремий VEVENT для кожного року в вікні
            for year in range(CURRENT_YEAR, CURRENT_YEAR + ICS_EASTER_WINDOW_YEARS + 1):
                start = resolve_date(h.rule, year)
                e = Event()
                e.add("uid", f"{h.slug}-{year}@dityam.com.ua")
                e.add("summary", title)
                e.add("description", desc)
                e.add("dtstart", start)
                e.add("dtend", start + timedelta(days=1))
                e.add("categories", [h.category])
                e.add("transp", "TRANSPARENT")
                cal.add_component(e)

    return cal.to_ical()


# -------------------- ГЕНЕРАЦІЯ JSON ДЛЯ UI --------------------
def build_json() -> dict:
    """Матеріалізує всі події на вікно років для рендерингу в Next.js."""
    by_year: dict[str, list[dict]] = {}

    for year in range(CURRENT_YEAR, CURRENT_YEAR + JSON_WINDOW_YEARS):
        events = []
        for h in HOLIDAYS:
            d = resolve_date(h.rule, year)
            events.append({
                "slug": h.slug,
                "emoji": h.emoji,
                "summary": h.summary,
                "description": h.description,
                "category": h.category,
                "tags": h.tags,
                "date": d.isoformat(),
                "month": d.month,
                "day": d.day,
                "dow": d.isoweekday(),  # 1=Mon..7=Sun
            })
        # Сортування за датою
        events.sort(key=lambda x: x["date"])
        by_year[str(year)] = events

    return {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "current_year": CURRENT_YEAR,
        "years": list(by_year.keys()),
        "by_year": by_year,
    }


# -------------------- MAIN --------------------
if __name__ == "__main__":
    ICS_OUT.parent.mkdir(parents=True, exist_ok=True)
    JSON_OUT.parent.mkdir(parents=True, exist_ok=True)

    ics_bytes = build_ics()
    ICS_OUT.write_bytes(ics_bytes)
    print(f"✅ ICS:  {ICS_OUT} ({len(ics_bytes):,} bytes)")

    data = build_json()
    JSON_OUT.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
    total = sum(len(v) for v in data["by_year"].values())
    print(f"✅ JSON: {JSON_OUT} ({total} подій на {len(data['years'])} років)")
